package remote.wise.pojo;

public class WhatsappReportPOJO 
{
	String AssetID;
	String CustomerCode;
	String MobileNumber;
	int NumberOfNotificationsPerDay;
	
	
	public String getAssetID() {
		return AssetID;
	}
	public void setAssetID(String assetID) {
		AssetID = assetID;
	}
	public String getCustomerCode() {
		return CustomerCode;
	}
	public void setCustomerCode(String customerCode) {
		CustomerCode = customerCode;
	}
	public String getMobileNumber() {
		return MobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		MobileNumber = mobileNumber;
	}
	public int getNumberOfNotificationsPerDay() {
		return NumberOfNotificationsPerDay;
	}
	public void setNumberOfNotificationsPerDay(int numberOfNotificationsPerDay) {
		NumberOfNotificationsPerDay = numberOfNotificationsPerDay;
	}
	
	
}
