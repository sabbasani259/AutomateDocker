package com.wipro.mcoreapp.businessobject;

public class AssetDetailsEntity 
{
	public String assetId;
	public String machineNum;
	public String engineNum;
	public String imei;
	public String iccidNum;
	public String rollOffDate;
	
	public String getAssetId() {
		return assetId;
	}
	public void setAssetId(String assetId) {
		this.assetId = assetId;
	}
	public String getMachineNum() {
		return machineNum;
	}
	public void setMachineNum(String machineNum) {
		this.machineNum = machineNum;
	}
	public String getEngineNum() {
		return engineNum;
	}
	public void setEngineNum(String engineNum) {
		this.engineNum = engineNum;
	}
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public String getIccidNum() {
		return iccidNum;
	}
	public void setIccidNum(String iccidNum) {
		this.iccidNum = iccidNum;
	}
	public String getRollOffDate() {
		return rollOffDate;
	}
	public void setRollOffDate(String rollOffDate) {
		this.rollOffDate = rollOffDate;
	}
	
	
}
